#include "calc.h"
/**
 *op_add - adds two int
 * @a: int 1
 * @b: int 2
 * Return: sum
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two int
 * @a: int 1
 * @b: int 2
 * Return: sub
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - two int multiplication
 * @a: int 1
 * @b: int 2
 * Return: mul
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - two int division
 * @a: int 1
 * @b: int 2
 * Return: division of 2 ints
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod -  two int mod
 * @a: int 1
 * @b: int 2
 * Return: mod of 2 ints
*/
int op_mod(int a, int b)
{
	return (a % b);
}
